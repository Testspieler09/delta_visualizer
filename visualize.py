@app.cell(hide_code=True)
def _():
    csv_file_browser = mo.ui.file_browser(
        label="Select the benchmarks zou want to compare",
        filetypes=["csv"],
        selection_mode="file",
        multiple=True,
    ).form()

    # TODO: render all the ui in a vstack and move the logic into another column / cell so it is way cleaner
    csv_file_browser
    return (csv_file_browser,)


@app.cell(hide_code=True)
def _(csv_file_browser):
    warnings = []
    datasets = []

    if (
        csv_file_browser.value is None
        or csv_file_browser.value == ()
        or csv_file_browser.value[0].path == ()
    ):
        warnings.append("No files selected.")
    else:
        for csv in csv_file_browser.value:
            try:
                ds = load_dataset(csv.path)
                datasets.append(ds)
            except ValueError as e:
                warnings.append(f"The selected csv file '{csv.name}' is not a file generated by delta.")
                warnings.append(str(e))
    return datasets, warnings


@app.cell(hide_code=True)
def _(warnings):
    if warnings:
        warning_str = "CSV data could not be loaded:\n\n" + "\n".join(
            f"- {w}" for w in warnings
        )
        output = mo.callout(
            mo.plain_text(warning_str),
            kind="warn",
        )
    else:
        output = mo.callout(
            "Successfully loaded benchmark data",
            kind="success",
        )

    output
    return


@app.cell(hide_code=True)
def _(datasets):
    registry = DatasetRegistry()
    for set in datasets:
        try:
            registry.add(set)
        except ValueError as e:
            print(str(e))
    return (registry,)


@app.cell
def _(datasets):
    options = {
        f"{ds.display_name} ({ds.type})": ds.id
        for ds in datasets
    }

    # TODO: make it possible to createe multiple at once
    group_creation_form = ( 
        mo.md('''
        **Create plot groups.**

        {name}

        {datasets}
        ''')
        .batch(
            name=mo.ui.text(label="Groupname"),
            datasets=mo.ui.multiselect(label="Select datasets for group", options=options),
        )
        .form(
            show_clear_button=True,
            bordered=True,
            submit_button_label="Create Group"
        )
    )

    group_creation_form
    return (group_creation_form,)


@app.cell
def _(group_creation_form, registry):
    group = None
    error = None

    if group_creation_form.value:
        try:
            group = create_group(
                registry,
                group_creation_form.value["name"],
                group_creation_form.value["datasets"],
            )
        except ValueError as e:
            error = str(e)
    return (group,)


