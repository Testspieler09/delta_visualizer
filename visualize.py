@app.cell(hide_code=True)
def _(csv_file_browser):
    warnings = []
    datasets = []

    if (
        csv_file_browser.value is None
        or csv_file_browser.value == ()
        or csv_file_browser.value[0].path == ()
    ):
        warnings.append("No files selected.")
    else:
        for csv in csv_file_browser.value:
            try:
                ds = load_dataset(csv.path)
                datasets.append(ds)
            except ValueError as e:
                warnings.append(
                    f"The selected csv file '{csv.name}' is not a file generated by delta."
                )
                warnings.append(str(e))
    return datasets, warnings


@app.cell(hide_code=True)
def _(warnings):
    if warnings:
        warning_str = "CSV data could not be loaded:\n\n" + "\n".join(
            f"- {w}" for w in warnings
        )
        output = mo.callout(
            mo.plain_text(warning_str),
            kind="warn",
        )
    else:
        output = mo.callout(
            "Successfully loaded benchmark data",
            kind="success",
        )

    output
    return


