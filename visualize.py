@app.cell(hide_code=True)
def _(datasets):
    options = {f"{ds.display_name} ({ds.type})": ds.id for ds in datasets}
    _label_identifier = "Dataset name"

    group_name_input = mo.ui.text(label="Groupname")
    dataset_table_input = mo.ui.table(
        data=[{_label_identifier: name} for name in options.keys()],
        selection="multi",
        label="Select datasets"
    )

    def handle_create_group(form_value):
        if not form_value or not form_value["name"]:
            return
        if any(g.name == form_value["name"] for g in get_groups().values()):
            return

        selected_names = [row[_label_identifier] for row in form_value["datasets"]]
        selected_ids = [options[name] for name in selected_names]

        new_group = create_group(registry, form_value["name"], selected_ids)
        group_id = getattr(new_group, 'id', str(uuid.uuid4()))
        set_groups({**get_groups(), group_id: new_group})


    return dataset_table_input, group_name_input, handle_create_group


