@app.cell
def _(config_form):
    config = None

    if config_form.value:
        def list_to_dict(table_data, key_col="Key", val_col="Value"):
            return {
                row[key_col]: row[val_col] 
                for row in table_data 
                if row.get(key_col) and str(row[key_col]).strip() != ""
            }

        config = PlotConfig(
            memory_metric=config_form.value["memory_metric"],
            memory_unit=config_form.value["memory_unit"],
            timeline_alignment=config_form.value["timeline_alignment"],
            y_scale=config_form.value["y_scale"],
            palette=list_to_dict(
                config_form.value["color_map"], 
                key_col="Column Identifier", 
                val_col="Color (Hex)"
            ),
            label_map=list_to_dict(
                config_form.value["label_map"], 
                key_col="Column Identifier", 
                val_col="Legend Label"
            ),
        )

    app_state = AppState(registry=registry, groups=get_groups(), config=config)
    return (app_state,)


@app.cell
def _(app_state):
    plots = app_state.plot_all()

    if len(plots) == 1:
        mo.output.append(plots)
    elif len(plots) > 1:
        mo.output.append(mo.carousel(plots))
    else:
        mo.output.append(mo.callout("Nothing to plot", kind="info"))
    
    AppState.close_all_plots(plots)
    return


if __name__ == "__main__":
    app.run()
