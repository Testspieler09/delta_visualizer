@app.cell
def _(config_form):
    config = None

    if config_form.value:
        config = PlotConfig(
            memory_metric=config_form.value["memory_metric"],
            memory_unit=config_form.value["memory_unit"],
            timeline_alignment=config_form.value["timeline_alignment"],
            y_scale=config_form.value["y_scale"],
        )
    return (config,)


@app.cell
def _(config, group, registry):
    if group is None:
        final_output = mo.callout("No group created.", kind="warn")
    if config is None:
        final_output = mo.callout("No plot configuration set", kind="warn")

    if group is not None and config is not None:
        try:
            final_output = plot_group(group, registry, config)
        except Exception as e:
            final_output = mo.callout(str(e), kind="warn")

    final_output
    return


